def count_letters (text):
    def_count = 0
    let_dict = {}
    low_text = text.lower()
    symb_list=list(low_text)
    for symb in symb_list:
        if symb.isalpha():
            let_dict[symb] = let_dict.get(symb, def_count) + 1
    return let_dict

def calculate_frequency(text, let_dict):
    symb_num = 0
    for symb in text:
        if symb.isalpha():
            symb_num += 1
    for let in let_dict:
        print(let,f": {round(let_dict.get(let)/symb_num, 2)}")

